function replaceMergeFields(html: string, values: Record<string, string | undefined>) {
  return html.replace(/\{\{(\w+)\}\}/g, (match, key) => values[key] ?? match);
}

function parseThemeRecord(record?: EmailThemeRecord): ThemeSettings | null {
  if (!record) return null;
  try {
    return JSON.parse(record.themeJson) as ThemeSettings;
  } catch {
    return null;
  }
}

function parseThemeJson(themeJson?: string): ThemeSettings | null {
  if (!themeJson) return null;
  try {
    return JSON.parse(themeJson) as ThemeSettings;
  } catch {
    return null;
  }
}

function stripAppliedThemeCss(css = "") {
  const start = css.indexOf(GENERATED_THEME_START);
  const end = css.indexOf(GENERATED_THEME_END);
  if (start === -1 || end === -1 || end < start) return css;
  return `${css.slice(0, start)}${css.slice(end + GENERATED_THEME_END.length)}`.trim();
}

function stripThemeCss(css = "") {
  const withoutAppliedTheme = stripAppliedThemeCss(css);
  return withoutAppliedTheme.includes(LEGACY_GENERATED_THEME_MARKER) ? "" : withoutAppliedTheme;
}

function buildTemplateCss(settings: ThemeSettings, existingCss = "") {
  const customCss = stripThemeCss(existingCss);
  const themeCss = `${GENERATED_THEME_START}\n${generateCss(settings)}\n${GENERATED_THEME_END}`;
  return customCss ? `${themeCss}\n\n${customCss}` : themeCss;
}

function upsertClassName(html: string, className: string) {
  const match = html.match(/class=(["'])(.*?)\1/i);
  if (!match) return html.replace(/^<([a-z][\w:-]*)(\s|>)/i, `<$1 class="${className}"$2`);
  const current = match[2].split(/\s+/).filter(Boolean);
  if (current.includes(className)) return html;
  return html.replace(match[0], `class=${match[1]}${[...current, className].join(" ")}${match[1]}`);
}

function normalizeTemplateHtmlForTheme(html: string, settings: ThemeSettings) {
  const source = (html || starterTemplateHtml).trim();
  if (source.includes('data-mana-theme="true"')) {
    return source
      .replace(/data-mana-theme-name=(["']).*?\1/i, `data-mana-theme-name="${settings.themeName}"`)
      .replace(/data-mana-theme-updated=(["']).*?\1/i, `data-mana-theme-updated="${new Date().toISOString()}"`);
  }

  const rootTableMatch = source.match(/<table\b[^>]*>/i);
  let themed = source;
  if (rootTableMatch) {
    let rootTable = rootTableMatch[0];
    rootTable = upsertClassName(rootTable, "email-container");
    if (!/data-mana-theme=/i.test(rootTable)) {
      rootTable = rootTable.replace(/>$/, ` data-mana-theme="true" data-mana-theme-name="${settings.themeName}" data-mana-theme-updated="${new Date().toISOString()}">`);
    }
    themed = source.replace(rootTableMatch[0], rootTable);
  }

  if (!/class=(["'])[^"']*\bemail-wrapper\b/i.test(themed)) {
    themed = `<div class="email-wrapper" data-mana-theme="true" data-mana-theme-name="${settings.themeName}" data-mana-theme-updated="${new Date().toISOString()}">\n${themed}\n</div>`;
  }

  return themed
    .replace(/<a\b(?![^>]*class=)/gi, '<a class="btn-primary"')
    .replace(/<td([^>]*?)style=(["'])(?=[^"']*background:\s*#0f766e)/i, '<td class="email-header"$1style=$2')
    .replace(/<td([^>]*?)style=(["'])(?=[^"']*padding:28px 32px)/i, '<td class="email-body"$1style=$2')
    .replace(/<td([^>]*?)style=(["'])(?=[^"']*background:\s*#f9fafb)/i, '<td class="email-footer"$1style=$2');
}

function buildAppliedThemeTemplate(template: WorkingTemplate, settings: ThemeSettings): WorkingTemplate {
  const generatedCss = generateCss(settings);
  return {
    ...template,
    html: normalizeTemplateHtmlForTheme(template.html, settings),
    css: buildTemplateCss(settings, template.css),
    themeName: settings.themeName,
    themeJson: JSON.stringify(settings),
    generatedCss,
  };
}

function themedPreviewDocument(html: string, css: string, settings: ThemeSettings, values: Record<string, string | undefined>) {
  const themedHtml = replaceMergeFields(normalizeTemplateHtmlForTheme(html || starterTemplateHtml, settings), values);
  return `
    <!doctype html>
    <html>
      <head>
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <style>
          ${buildTemplateCss(settings, css)}
          body { margin:0; background:${settings.backgroundColor}; color:${settings.textColor}; font-family:'${settings.bodyFont}',Arial,sans-serif; }
          body table[role="presentation"] { font-family:'${settings.bodyFont}',Arial,sans-serif !important; }
          body table[role="presentation"] table[role="presentation"] { max-width:${settings.containerWidth}px !important; }
          body td[style*="background:#0f766e"],
          body td[style*="background:#0f172a"],
          body td[style*="background: #0f766e"],
          body td[style*="background: #0f172a"] { background:${settings.primaryColor} !important; color:#ffffff !important; }
          body h1 { font-family:'${settings.headingFont}',Arial,sans-serif !important; font-size:${settings.headingSize}px !important; }
          body h2 { font-family:'${settings.headingFont}',Arial,sans-serif !important; color:${settings.textColor} !important; }
          body a { background:${settings.buttonStyle === "gradient" ? `linear-gradient(135deg,${settings.primaryColor},${settings.accentColor})` : settings.primaryColor} !important; color:${settings.buttonTextColor} !important; border-radius:${settings.buttonRadius}px !important; }
        </style>
      </head>
      <body>${themedHtml}</body>
    </html>
  `;
}

function exportJson(template: WorkingTemplate | EmailTemplateRecord) {
  const blob = new Blob([JSON.stringify(template, null, 2)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `${"templateName" in template ? template.templateName : template.name}.json`;
  link.click();
  URL.revokeObjectURL(url);
}
// ─── ThemeApplyPanel ─────────────────────────────────────────────────────────
// Shown below the ThemeBuilder in the Theme tab.
// Lets admin select individual templates, preview them with the active theme,
// and apply the CSS to each one.

function ThemeApplyPanel({
  templates,
  settings,
  sampleValues,
  applying,
  onApply,
  onPreview,
}: {
  templates: EmailTemplateRecord[];
  settings: ThemeSettings;
  sampleValues: Record<string, string | undefined>;
  applying: boolean;
  onApply: (selected: EmailTemplateRecord[]) => void;
  onPreview: (template: EmailTemplateRecord) => void;
}) {
  const [selected, setSelected] = useState<Set<number | string>>(new Set());

  const toggle = (id: number | string) => {
    setSelected((prev) => {
      const next = new Set(prev);
      if (next.has(id)) next.delete(id);
      else next.add(id);
      return next;
    });
  };

  const toggleAll = () => {
    if (selected.size === templates.length) {
      setSelected(new Set());
    } else {
      setSelected(new Set(templates.map((t) => t.id ?? t.name)));
    }
  };

  const selectedTemplates = templates.filter((t) => selected.has(t.id ?? t.name));

  if (templates.length === 0) return null;

  return (
    <div className="mt-5 rounded-2xl border border-border bg-card p-5 shadow-sm space-y-4">
      {/* Header */}
      <div className="flex flex-wrap items-center justify-between gap-3">
        <div>
          <h3 className="flex items-center gap-2 text-sm font-bold text-foreground">
            <PaintBucket className="h-4 w-4 text-primary" />
            Apply &quot;{settings.themeName}&quot; to Email Templates
          </h3>
          <p className="mt-0.5 text-[11px] text-muted-foreground">
            Select templates to render their HTML with this theme&apos;s CSS. Saved templates will display the themed preview.
          </p>
        </div>
        <div className="flex items-center gap-2">
          <button
            type="button"
            onClick={toggleAll}
            className="inline-flex items-center gap-1.5 rounded-xl border border-border px-3 py-1.5 text-xs font-bold text-foreground hover:bg-input cursor-pointer transition-all"
          >
            {selected.size === templates.length ? <SquareCheck className="h-4 w-4 text-primary" /> : <Square className="h-4 w-4" />}
            {selected.size === templates.length ? "Deselect All" : "Select All"}
          </button>
          <button
            type="button"
            disabled={selected.size === 0 || applying}
            onClick={() => onApply(selectedTemplates)}
            className="inline-flex items-center gap-1.5 rounded-xl bg-primary px-4 py-1.5 text-xs font-bold text-white hover:bg-primary/90 disabled:opacity-60 cursor-pointer transition-all"
          >
            {applying
              ? <RefreshCw className="h-4 w-4 animate-spin" />
              : <Check className="h-4 w-4" />}
            Apply to {selected.size > 0 ? `${selected.size} Template${selected.size > 1 ? "s" : ""}` : "Selected"}
          </button>
        </div>
      </div>

      {/* Template grid */}
      <div className="grid grid-cols-1 gap-3 sm:grid-cols-2 xl:grid-cols-3 2xl:grid-cols-4">
        {templates.map((template) => {
          const key = template.id ?? template.name;
          const isSelected = selected.has(key);
          const alreadyUsesTheme = template.themeName === settings.themeName;
          const previewDoc = themedPreviewDocument(template.html, template.css ?? "", settings, sampleValues);

          return (
            <div
              key={key}
              onClick={() => toggle(key)}
              className={`group relative cursor-pointer rounded-2xl border-2 bg-background p-3 transition-all ${
                isSelected ? "border-primary shadow-md shadow-primary/10" : "border-border hover:border-primary/30"
              }`}
            >
              {/* Checkbox top-left */}
              <div className={`absolute left-3 top-3 z-10 rounded-md p-0.5 ${isSelected ? "text-primary" : "text-muted-foreground"}`}>
                {isSelected ? <SquareCheck className="h-4 w-4" /> : <Square className="h-4 w-4" />}
              </div>

              {/* Already themed badge */}
              {alreadyUsesTheme && (
                <div className="absolute right-3 top-3 z-10 flex items-center gap-1 rounded-full bg-emerald-500/10 px-2 py-0.5 text-[10px] font-bold text-emerald-600 dark:text-emerald-400">
                  <Check className="h-3 w-3" />
                  Applied
                </div>
              )}

              {/* Mini themed preview iframe */}
              <div className="h-32 overflow-hidden rounded-xl border border-border bg-slate-100 mb-2.5">
                <iframe
                  title={`${template.name} preview`}
                  srcDoc={previewDoc}
                  sandbox="allow-same-origin"
                  className="h-[400px] w-[300%] origin-top-left scale-[0.333] border-0 pointer-events-none"
                />
              </div>

              {/* Template info */}
              <div className="space-y-1 pl-6">
                <p className="truncate text-xs font-bold text-foreground">{template.name}</p>
                <p className="truncate text-[10px] text-muted-foreground">{template.subject || "No subject"}</p>
                <div className="flex items-center gap-1.5 flex-wrap">
                  <span className="rounded-full bg-muted px-2 py-0.5 text-[10px] font-semibold text-muted-foreground">{template.status}</span>
                  {template.themeName && (
                    <span className="rounded-full bg-primary/10 px-2 py-0.5 text-[10px] font-bold text-primary truncate max-w-[8rem]">{template.themeName}</span>
                  )}
                </div>
              </div>

              {/* Preview button */}
              <button
                type="button"
                onClick={(e) => { e.stopPropagation(); onPreview(template); }}
                className="mt-2.5 w-full inline-flex items-center justify-center gap-1.5 rounded-xl border border-border px-2 py-1.5 text-[11px] font-bold text-foreground hover:bg-primary/5 hover:border-primary/30 cursor-pointer transition-all"
              >
                <Eye className="h-3.5 w-3.5 text-primary" />
                Preview with Theme
              </button>
            </div>
          );
        })}
      </div>
    </div>
  );
}

