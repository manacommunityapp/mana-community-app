  const [themePreviewTemplate, setThemePreviewTemplate] = useState<EmailTemplateRecord | null>(null);

  useEffect(() => {
    async function loadCommunities() {
      try {
        const list = await communityService.getCommunities();
        setCommunities(list);
        setCommunityId((prev) => prev ?? user?.communityId ?? list[0]?.id ?? null);
      } catch {
        showError("Failed to load communities");
      }
    }
    void loadCommunities();
  }, [user?.communityId]);

  const loadTemplates = useCallback(async () => {
    if (communityId == null) return;
    setLoading(true);
    try {
      const result = await emailTemplateService.list(communityId);
      setTemplates(result);
    } catch {
      setTemplates([]);
      showWarning("Email template API is not available yet. You can still compose a new template.");
    } finally {
      setLoading(false);
    }
  }, [communityId]);

  useEffect(() => {
    void loadTemplates();
  }, [loadTemplates]);

  const loadSavedThemes = useCallback(async () => {
    if (communityId == null) return;
    setThemesLoading(true);
    try {
      const list = await themePersistenceService.list(communityId);
      setSavedThemes(list);
      const preferredTheme = parseThemeRecord(list.find((theme) => theme.isDefault) ?? list[0]);
      if (preferredTheme) {
        setActiveThemeSettings(preferredTheme);
      }
    } catch {
      setSavedThemes([]);
      setActiveThemeSettings(DEFAULT_THEME);
    } finally {
      setThemesLoading(false);
    }
  }, [communityId]);

  useEffect(() => {
    void loadSavedThemes();
  }, [loadSavedThemes]);

  const filteredCommunities = useMemo(() => {
    if (!user?.communityId) return communities;
    return communities.filter((c) => c.id === user.communityId);
  }, [communities, user?.communityId]);

  const filteredTemplates = useMemo(() => {
    const term = search.trim().toLowerCase();
    return templates.filter((template) => {
      const categoryMatch = selectedCategory === "All" || template.category === selectedCategory;
      const termMatch = !term || `${template.name} ${template.subject} ${template.status} ${template.category ?? ""} ${(template.tags ?? []).join(" ")}`.toLowerCase().includes(term);
      return categoryMatch && termMatch;
    });
  }, [templates, search, selectedCategory]);

